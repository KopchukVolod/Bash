#!/bin/bash

# Запит першого рядка
echo "Введіть перший рядок:"
read -r first_string

# Запит другого рядка
echo "Введіть другий рядок:"
read -r second_string

# Перевірка довжини першого рядка
if [ -z "$first_string" ]; then
    echo "Перший рядок має нульову довжину."
else
    echo "Перший рядок не має нульової довжини."
fi

# Перевірка довжини другого рядка
if [ -n "$second_string" ]; then
    echo "Другий рядок має ненульову довжину."
else
    echo "Другий рядок має нульову довжину."
fi
